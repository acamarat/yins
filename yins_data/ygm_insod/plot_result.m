avp = load('./ygm_circle_avp.txt');
time = avp(:,10);
figure;
RE = 6378137; 
dlat = (avp(:,7) - avp(1,7)) * RE;
dlon = (avp(:,8) - avp(1,8)) * RE ./sin(avp(:,7));
status = zeros(length(dlat),1);
status(time >300) = 1; 
status(end) = 1.2;status(1) = -0.2;
scatter(dlat, dlon, [], status, '.'); hold on;
colormap jet;
grid on; box on;
xlabel('Position Error of North(m)');
ylabel('Position Error of East(m)'); 
figure
subplot(3,1,1);
scatter(time, avp(:,4), [], status,'.');
legend('East');
ylabel('m/s');
grid on; box on;
subplot(3,1,2);
scatter(time, avp(:,5), [], status,'.');
grid on; box on;
legend('North');
ylabel('m/s');
subplot(3,1,3);
scatter(time, avp(:,6), [], status,'.');
grid on; box on;
legend('Up');
ylabel('m/s');
xlabel('time series(sec)');
colormap jet;